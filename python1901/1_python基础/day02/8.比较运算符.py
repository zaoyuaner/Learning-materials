'''
比较运算符：
==  等于
!=  不等于
>   大于
<  小于
>=  大于等于
<=  小于等于
比较运算符返回是一个bool值【True/False】
'''
a = 10
b = 20
print(a == b)  #False
print(a != b) #True
print(a > b)  #False
print(a < b)  #True
print(a >= b)  #False
print(a <= b)  #True
