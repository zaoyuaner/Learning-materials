# 9*9乘法表
# 循环嵌套一般是两层
# 1x1=1                                                   1             1
# 1x2=2 2x2=4                                             2             2
# 1x3=3 2x3=6 3x3=9                                       3             3
# .....
# 逻辑: 需要打印9行 , 每一行中,打印对应的乘法结果.

row = 1
while row <=9:
	i = 1
	while i <= row :
		print(i,"*",row,"=",i*row , end="  ")    # 打印后,不换行
		i += 1
	print("")       # 打印空字符 实际上就是换行.
	row += 1
# 内层循环用于控制列打印次数,外层循环用于控制行数.